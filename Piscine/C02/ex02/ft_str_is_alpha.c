int	ft_str_is_alpha(char *str)
{
	while (*str != '\0')
	{
		if (*str < 'A' || *str > 'z')
		{
			return (0);
		}
		else if (*str > 'Z' && *str < 'a')
		{
			return (0);
		}
		str++;
	}
	return (1);
}
