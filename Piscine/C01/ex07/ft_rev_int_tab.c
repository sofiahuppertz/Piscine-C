void	ft_rev_int_tab(int *tab, int size)
{
	int	temp;
	int	start;
	int	end;

	end = size - 1;
	start = 0;
	while (start < end)
	{
		temp = tab[start];
		tab[start] = tab[end];
		tab[end] = temp;
		start++;
		end--;
	}
}
